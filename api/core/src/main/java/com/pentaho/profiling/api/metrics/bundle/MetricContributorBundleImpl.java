package com.pentaho.profiling.api.metrics.bundle;

import com.pentaho.profiling.api.classes.HasClasses;

import java.util.ArrayList;
import java.util.List;

/**
 * Created by bryan on 3/9/15.
 */
public class MetricContributorBundleImpl implements MetricContributorBundle, HasClasses {
  private String name;
  private List<Class> classes;

  public MetricContributorBundleImpl() {
    classes = new ArrayList<Class>();
  }

  @Override public List<Class> getClasses() {
    return classes;
  }

  public void setClasses( List<Class> classes ) {
    this.classes = classes;
  }

  @Override
  public String getName() {
    return name;
  }

  public void setName( String name ) {
    this.name = name;
  }

  //OperatorWrap isn't helpful for autogenerated methods
  //CHECKSTYLE:OperatorWrap:OFF

  @Override public boolean equals( Object o ) {
    if ( this == o ) {
      return true;
    }
    if ( o == null || getClass() != o.getClass() ) {
      return false;
    }

    MetricContributorBundleImpl that = (MetricContributorBundleImpl) o;

    if ( name != null ? !name.equals( that.name ) : that.name != null ) {
      return false;
    }
    return !( classes != null ? !classes.equals( that.classes ) : that.classes != null );

  }

  @Override public int hashCode() {
    int result = name != null ? name.hashCode() : 0;
    result = 31 * result + ( classes != null ? classes.hashCode() : 0 );
    return result;
  }

  @Override public String toString() {
    return "MetricContributorBundleImpl{" +
      "name='" + name + '\'' +
      ", classes=" + classes +
      '}';
  }

  //CHECKSTYLE:OperatorWrap:ON
}
